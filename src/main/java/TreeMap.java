public class TreeMap {

    public static void main(String[] args) {
        java.util.TreeMap<Integer, String> treeMap = new java.util.TreeMap<>();

        treeMap.put(1, "one");
        System.out.println(treeMap);
        treeMap.put(5, "five");
        System.out.println(treeMap);
        treeMap.put(3, "three");
        System.out.println(treeMap);
        treeMap.put(9, "nine");
        System.out.println(treeMap);
        treeMap.put(7, "seven");
        System.out.println(treeMap);

    }
}
